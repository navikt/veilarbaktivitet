

CREATE VIEW DVH_MOTE AS (
  SELECT
    AKTIVITET_ID,
    VERSJON,
    ADRESSE,
    FORBEREDELSER,
    KANAL,
    REFERAT,
    REFERAT_PUBLISERT
  FROM MOTE
);

