  OPPRETTET_DATO             TIMESTAMP(6)   NOT NULL,
  OPPRETTET_AV               NVARCHAR2(255) NOT NULL,
  ENDRET_DATO                TIMESTAMP(6),
  ENDRET_AV                  NVARCHAR2(255)
);

